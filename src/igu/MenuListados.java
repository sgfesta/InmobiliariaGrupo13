    private void rellenarCamposInquilinos() {

        // relleno campos pestaña inquilinos
        String dniInquilino = jTDNIInquilinos.getText();
        Inquilino inquilinoSelecionado = inquilinoD.buscarInquilinoPorDni(dniInquilino);
        // boolean activo = jrbEstado.isSelected();

        if (inquilinoSelecionado != null) {
            jTIDInquilino.setText(String.valueOf(inquilinoSelecionado.getIdInquilino()));
            jTNombreInquilinos.setText(inquilinoSelecionado.getNombre());
            jTApellidoInquilinos.setText(inquilinoSelecionado.getApellido());
            jTDNIInquilinos.setText(String.valueOf(inquilinoSelecionado.getDni()));
            jTCuitInquilinos.setText(String.valueOf(inquilinoSelecionado.getCuit()));
            jTLugarTrabajoInquilinos.setText(inquilinoSelecionado.getLugarTrabajo());
            jRInquilino.setSelected(inquilinoSelecionado.isActivo());
            habilitarBotonesInquilinos();

        } else if (jTDNIInquilinos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un DNI antes de realizar una busqueda");
        } else if (inquilinoSelecionado == null) {
            JOptionPane.showMessageDialog(this, "No existe un Inquilino con ese DNI");
            jTDNIInquilinos.setText("");
        }
    }

